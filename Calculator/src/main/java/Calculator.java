public class Calculator {
    public Calculator() {

    };

    public int addNumbers(int i, int j) {
        return i + j;
    }


    public int subtractTwoNumbers(int i, int j) {
        return i - j;
    }

    public int multiply(int i, int j) {
        return i * j;
    }

    public int divide(int i, int j) {
        return i / j;
    }
}
